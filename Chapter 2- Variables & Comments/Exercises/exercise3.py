#Exercise 3: Stripping Names

# assigning the name variable

name = "\tMaryel Gregorio\n"

# printing the name normally

print("Unmodified:")
print(name)

# printing  while using lstrip

print("\nUsing lstrip():")
print(name.lstrip())

# printing while using rstrip

print("\nUsing rstrip():")
print(name.rstrip())

# printing while using strip

print("\nUsing strip():")
print(name.strip())
