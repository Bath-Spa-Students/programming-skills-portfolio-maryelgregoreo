"""Write a python program that stores fruit names in a list named fruits. Unpack the list into three
variables x, y and z and then print the values of each variable."""

# listing fruits

fruits = ["banana", "orange", "mango", "grapes", "peach", "blueberry"]

# using slice to unpack the list into x, y and z

x = fruits [0:2]
y = fruits [2:4]
z = fruits [4:6]

# printing

print(x)
print(y)
print(z)