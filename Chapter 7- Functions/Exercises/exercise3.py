## Exercise 3: T-Shirt 

# printing the size and message

def make_shirt(size, message):
    print (f'The t-shirt I will make is {size}!')
    print (f'The message is {message}')

# assigning a size and message function

make_shirt ("medium", "IM FEARLESS!")

# assigning a second size and message function

make_shirt (size = "large", message = "LE SSERAFIM")
